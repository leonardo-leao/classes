k=0:9;
N=length(k);

A = 60/7;
B = 10/7;

for i=1:N
    j = i-1;
    if j<3
        ya(j+1)=(A*(0.3)^(j)+B);
    else
        ya(j+1)=(A*(0.3)^(j)+B)-(A*(0.3)^(j-3)+B);
    end
end


yr(1)=10;
for j=1:N-1
    if j<3
        yr(j+1)=0.3*yr(j)+1;
    else
        yr(j+1)=0.3*yr(j);
    end
end

ya
yr
stairs(k,ya)
hold on
stairs(k,yr, '*')
hold off